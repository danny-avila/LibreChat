#!/usr/bin/env bash

set -euo pipefail

OS="${1:-linux}"
ARCH="${2:-amd64}"

require_command() {
  local command="$1"
  if ! command -v "$command" >/dev/null 2>&1; then
    echo "Missing required command: $command" >&2
    exit 1
  fi
}

image_repository() {
  local image="$1"
  local last

  image="${image%@*}"
  last="${image##*/}"

  if [[ "$last" == *:* ]]; then
    printf '%s\n' "${image%:*}"
    return
  fi

  printf '%s\n' "$image"
}

pin_image() {
  local variable="$1"
  local source="$2"
  local repository
  local digest

  repository="$(image_repository "$source")"
  digest="$(skopeo inspect --override-os "$OS" --override-arch "$ARCH" "docker://$source" | jq -r '.Digest')"

  if [[ -z "$digest" || "$digest" == "null" ]]; then
    echo "Could not resolve digest for $source" >&2
    exit 1
  fi

  printf '# source: %s\n' "$source"
  printf '%s=%s@%s\n\n' "$variable" "$repository" "$digest"
}

require_command skopeo
require_command jq

cat <<EOF
# Image pins for $OS/$ARCH.
# Generated by deploy/ferretdb/bin/pin-images.sh.
#
# Review release notes and run the FerretDB compatibility harness before using
# regenerated pins in production.

EOF

pin_image GATEWAY_IMAGE 'nginx:1.27.0-alpine'
pin_image DOCUMENTDB_IMAGE 'ghcr.io/ferretdb/postgres-documentdb:17-0.107.0-ferretdb-2.7.0'
pin_image FERRETDB_IMAGE 'ghcr.io/ferretdb/ferretdb:2.7.0'
pin_image VALKEY_IMAGE 'valkey/valkey:8.1-alpine'
pin_image MEILI_IMAGE 'getmeili/meilisearch:v1.35.1'
pin_image SEAWEEDFS_IMAGE 'chrislusf/seaweedfs:latest'
pin_image MINIO_IMAGE 'quay.io/minio/minio:RELEASE.2025-09-07T16-13-09Z-cpuv1'
pin_image MINIO_MC_IMAGE 'quay.io/minio/mc:RELEASE.2025-08-13T08-35-41Z-cpuv1'

cat <<'EOF'
# Optional RAG images. These are inactive unless RAG_ENABLED=true and
# deploy-compose.ferretdb.rag.yml is included in COMPOSE_FILES.
#
EOF

pin_image PGVECTOR_IMAGE 'pgvector/pgvector:0.8.0-pg15-trixie'

