#!/usr/bin/env bash

# shellcheck source=deploy/ferretdb/bin/common.sh
source "$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)/common.sh"

OUTPUT_ENV_FILE="${OUTPUT_ENV_FILE:-$DEPLOY_DIR/.env}"
OBJECT_STORE_MODE="${OBJECT_STORE_MODE:-seaweedfs}"
RAG_ENABLED="${RAG_ENABLED:-false}"
IMAGE_PINS_FILE="${IMAGE_PINS_FILE:-$DEPLOY_DIR/images.linux-amd64.env}"
FORCE="${FORCE:-false}"
COPY_LIBRECHAT_YAML="${COPY_LIBRECHAT_YAML:-true}"

case "$OBJECT_STORE_MODE" in
  seaweedfs | external-s3 | minio) ;;
  *)
    echo "Unsupported OBJECT_STORE_MODE: $OBJECT_STORE_MODE" >&2
    exit 1
    ;;
esac

case "$RAG_ENABLED" in
  true | false) ;;
  *)
    echo "Unsupported RAG_ENABLED: $RAG_ENABLED" >&2
    exit 1
    ;;
esac

if [[ -e "$OUTPUT_ENV_FILE" && "$FORCE" != "true" ]]; then
  echo "Refusing to overwrite existing env file: $OUTPUT_ENV_FILE" >&2
  echo "Set FORCE=true to replace it." >&2
  exit 1
fi

if ! command -v openssl >/dev/null 2>&1; then
  echo "Missing required command: openssl" >&2
  exit 1
fi

rand_hex() {
  openssl rand -hex "$1"
}

pin_or_default() {
  local name="$1"
  local default="$2"
  local value=""

  if [[ -f "$IMAGE_PINS_FILE" ]]; then
    value="$(awk -F= -v key="$name" '$1 == key { value = substr($0, index($0, "=") + 1) } END { print value }' "$IMAGE_PINS_FILE")"
  fi

  if [[ -n "$value" ]]; then
    printf '%s\n' "$value"
  else
    printf '%s\n' "$default"
  fi
}

domain_client="${DOMAIN_CLIENT:-${SERVER_URL:-https://chat.example.com}}"
domain_server="${DOMAIN_SERVER:-${SERVER_URL:-https://chat.example.com}}"
compose_files="deploy-compose.ferretdb.yml"

if [[ "$OBJECT_STORE_MODE" == "seaweedfs" ]]; then
  compose_files="deploy-compose.ferretdb.yml,deploy-compose.ferretdb.seaweedfs.yml"
elif [[ "$OBJECT_STORE_MODE" == "external-s3" ]]; then
  compose_files="deploy-compose.ferretdb.yml,deploy-compose.ferretdb.external-s3.yml"
fi

if [[ "$RAG_ENABLED" == "true" ]]; then
  compose_files+=",deploy-compose.ferretdb.rag.yml"
fi

output_env_abs="$(absolute_path "$OUTPUT_ENV_FILE")"
mkdir -p "$(dirname "$OUTPUT_ENV_FILE")"

cat >"$OUTPUT_ENV_FILE" <<EOF
# Generated by deploy/ferretdb/bin/generate-env.sh.
# Review every value before first production boot.

COMPOSE_PROJECT_NAME=${COMPOSE_PROJECT_NAME:-librechat-ferretdb}
COMPOSE_FILES=$compose_files

LIBRECHAT_ENV_FILE=$output_env_abs
LIBRECHAT_YAML_PATH=./librechat.yaml
LIBRECHAT_BIND_ADDRESS=${LIBRECHAT_BIND_ADDRESS:-127.0.0.1}
LIBRECHAT_HTTP_PORT=${LIBRECHAT_HTTP_PORT:-3080}
LIBRECHAT_API_IMAGE=${LIBRECHAT_API_IMAGE:-librechat-api:ferretdb}

OBJECT_STORE_MODE=$OBJECT_STORE_MODE
RAG_ENABLED=$RAG_ENABLED

GATEWAY_IMAGE=$(pin_or_default GATEWAY_IMAGE 'nginx:1.27.0-alpine')
DOCUMENTDB_IMAGE=$(pin_or_default DOCUMENTDB_IMAGE 'ghcr.io/ferretdb/postgres-documentdb:17-0.107.0-ferretdb-2.7.0')
FERRETDB_IMAGE=$(pin_or_default FERRETDB_IMAGE 'ghcr.io/ferretdb/ferretdb:2.7.0')
VALKEY_IMAGE=$(pin_or_default VALKEY_IMAGE 'valkey/valkey:8.1-alpine')
MEILI_IMAGE=$(pin_or_default MEILI_IMAGE 'getmeili/meilisearch:v1.35.1')
SEAWEEDFS_IMAGE=$(pin_or_default SEAWEEDFS_IMAGE 'chrislusf/seaweedfs:latest')
MINIO_IMAGE=$(pin_or_default MINIO_IMAGE 'quay.io/minio/minio:RELEASE.2025-09-07T16-13-09Z-cpuv1')
MINIO_MC_IMAGE=$(pin_or_default MINIO_MC_IMAGE 'quay.io/minio/mc:RELEASE.2025-08-13T08-35-41Z-cpuv1')

NODE_MAX_OLD_SPACE_SIZE=${NODE_MAX_OLD_SPACE_SIZE:-6144}
DOMAIN_CLIENT=$domain_client
DOMAIN_SERVER=$domain_server
TRUST_PROXY=${TRUST_PROXY:-1}
HOST=0.0.0.0
PORT=3080
NODE_ENV=production
CONSOLE_JSON=true
DEBUG_LOGGING=false
DEBUG_CONSOLE=false
NO_INDEX=false

DOCUMENTDB_USER=${DOCUMENTDB_USER:-ferretdb}
DOCUMENTDB_PASSWORD=$(rand_hex 24)
MONGO_MAX_POOL_SIZE=${MONGO_MAX_POOL_SIZE:-50}
MONGO_MIN_POOL_SIZE=${MONGO_MIN_POOL_SIZE:-5}
MONGO_AUTO_INDEX=${MONGO_AUTO_INDEX:-true}
FERRETDB_TELEMETRY=${FERRETDB_TELEMETRY:-disable}

# LibreChat still uses REDIS_* variable names and a redis:// URI, but the
# compose service runs Valkey to avoid Redis 7.4+ RSALv2/SSPLv1 licensing.
REDIS_PASSWORD=$(rand_hex 24)
REDIS_KEY_PREFIX=${REDIS_KEY_PREFIX:-librechat}

MEILI_MASTER_KEY=$(rand_hex 32)

# Present for compose interpolation and for the commercially licensed MinIO path.
# The SeaweedFS override does not run these containers by default.
MINIO_ROOT_USER=${MINIO_ROOT_USER:-librechat-minio}
MINIO_ROOT_PASSWORD=$(rand_hex 24)
MINIO_BUCKET=${MINIO_BUCKET:-librechat}
MINIO_COMMERCIAL_LICENSE_CONFIRMED=${MINIO_COMMERCIAL_LICENSE_CONFIRMED:-false}
AWS_REGION=${AWS_REGION:-us-east-1}

# Optional RAG. The default commercial stack leaves this disabled so the
# unresolved external RAG image is not part of the runtime. To enable RAG, set
# RAG_ENABLED=true, include deploy-compose.ferretdb.rag.yml in COMPOSE_FILES,
# and fill in source/license review metadata below.
RAG_PORT=${RAG_PORT:-8000}
RAG_API_IMAGE=$(pin_or_default RAG_API_IMAGE 'registry.librechat.ai/danny-avila/librechat-rag-api-dev-lite@sha256:643294ce339cdcdcc297acbb0cfa513310e5e7ed1544c86cf8059a64f39615c4')
PGVECTOR_IMAGE=$(pin_or_default PGVECTOR_IMAGE 'pgvector/pgvector:0.8.0-pg15-trixie')
RAG_POSTGRES_DB=${RAG_POSTGRES_DB:-rag}
RAG_POSTGRES_USER=${RAG_POSTGRES_USER:-rag}
RAG_POSTGRES_PASSWORD=$(rand_hex 24)
RAG_API_SOURCE_URL=${RAG_API_SOURCE_URL:-}
RAG_API_SOURCE_REF=${RAG_API_SOURCE_REF:-}
RAG_API_SOURCE_LICENSE=${RAG_API_SOURCE_LICENSE:-}
RAG_API_LICENSE_REVIEWED=${RAG_API_LICENSE_REVIEWED:-false}

JWT_SECRET=$(rand_hex 32)
JWT_REFRESH_SECRET=$(rand_hex 32)
CREDS_KEY=$(rand_hex 32)
CREDS_IV=$(rand_hex 16)

ALLOW_EMAIL_LOGIN=${ALLOW_EMAIL_LOGIN:-true}
ALLOW_REGISTRATION=${ALLOW_REGISTRATION:-false}
ALLOW_SOCIAL_LOGIN=${ALLOW_SOCIAL_LOGIN:-false}

# Add provider credentials here, for example:
# OPENAI_API_KEY=
# ANTHROPIC_API_KEY=
# GOOGLE_KEY=
EOF

if [[ "$OBJECT_STORE_MODE" == "seaweedfs" ]]; then
  cat >>"$OUTPUT_ENV_FILE" <<EOF

SEAWEEDFS_ACCESS_KEY=${SEAWEEDFS_ACCESS_KEY:-sw$(rand_hex 12)}
SEAWEEDFS_SECRET_KEY=${SEAWEEDFS_SECRET_KEY:-$(rand_hex 32)}
SEAWEEDFS_BUCKET=${SEAWEEDFS_BUCKET:-librechat}
SEAWEEDFS_VOLUME_SIZE_LIMIT_MB=${SEAWEEDFS_VOLUME_SIZE_LIMIT_MB:-30000}
SEAWEEDFS_VOLUME_MAX=${SEAWEEDFS_VOLUME_MAX:-100}
EOF
elif [[ "$OBJECT_STORE_MODE" == "external-s3" ]]; then
  cat >>"$OUTPUT_ENV_FILE" <<EOF

AWS_ENDPOINT_URL=${AWS_ENDPOINT_URL:-http://s3.internal.example:8333}
AWS_ACCESS_KEY_ID=${AWS_ACCESS_KEY_ID:-change-me-s3-access-key}
AWS_SECRET_ACCESS_KEY=${AWS_SECRET_ACCESS_KEY:-change-me-s3-secret-key}
AWS_BUCKET_NAME=${AWS_BUCKET_NAME:-librechat}
AWS_FORCE_PATH_STYLE=${AWS_FORCE_PATH_STYLE:-true}
EOF
fi

chmod 0600 "$OUTPUT_ENV_FILE"

if [[ "$COPY_LIBRECHAT_YAML" == "true" && ! -e "$REPO_ROOT/librechat.yaml" ]]; then
  cp "$DEPLOY_DIR/librechat.yaml.example" "$REPO_ROOT/librechat.yaml"
fi

echo "Generated env file: $OUTPUT_ENV_FILE"
if [[ "$domain_client" == *example.com* || "$domain_server" == *example.com* ]]; then
  echo "Edit DOMAIN_CLIENT and DOMAIN_SERVER before production start."
fi
echo "Run: ENV_FILE=$OUTPUT_ENV_FILE deploy/ferretdb/bin/validate-env.sh"
